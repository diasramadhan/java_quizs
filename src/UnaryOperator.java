public class UnaryOperator {
    /**
     * Java unary operator
     *
     * The Java unary operators require only one operand, Unary operators are used
     * to perform various operations i.e. :
     * incrementing/dec rementing a value by one (i++,i--)
     *
     * Operator unary Java hanya membutuhkan satu operasi, operator Unary digunakan
     * untuk melakukan berbagai operasi yaitu:
     * menambah/mengurangi nilai satu per satu (i++,i--)
     */

    public static void main(String[] args) {
        int x = 10;
        System.out.println(x++);
        System.out.println(++x);
        System.out.println(x--);
        System.out.println(--x);
    }
}
